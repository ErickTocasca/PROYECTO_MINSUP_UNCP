a = int(input("Digite un número del 1 al 3: "))

if a == 1:
    print("Uno")
elif a == 2:
    print("Dos")
elif a == 3:
    print("Tres")
else:
    print("El número que digitaste no esta en el rango, vuelva a intentarlo")